using System;
using System.Collections.Generic;

namespace Zongsoft.Security.Membership.Options
{
	/// <summary>
	/// 表示成员安全管理配置的接口。
	/// </summary>
	public interface IConfiguration
	{
		/// <summary>
		/// 获取用户提供程序的配置信息。
		/// </summary>
		IUserOption User
		{
			get;
		}

		/// <summary>
		/// 获取授权配置信息。
		/// </summary>
		IAuthorizationOption Authorization
		{
			get;
		}

		/// <summary>
		/// 获取验证配置信息。
		/// </summary>
		IAuthenticationOption Authentication
		{
			get;
		}
	}
}
